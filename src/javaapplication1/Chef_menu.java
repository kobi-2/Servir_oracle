    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        if (textfield_id.getText() != null || textfield_name != null || textfield_amount != null) {
            String qry;
            PreparedStatement ps;
            Connection conn = OracleConnection();
            try {
                qry = "update menu set Amount=Amount+? where id=?";
                ps = conn.prepareStatement(qry);

                ps.setInt(1, Integer.parseInt(textfield_amount1.getText()));
                ps.setString(2, textfield_name1.getText());
                ps.setInt(2, Integer.parseInt(textfield_id1.getText()));

                int res = ps.executeUpdate();
                fillTable();
                if (res >= 1) {
                    //JOptionPane.showMessageDialog(null, "Items"+ " have been added.....");
                } else {
                    JOptionPane.showMessageDialog(null, "Item Insertion Failed ....");
                }
                
                conn.close();
                ps.close();
            
            } catch (HeadlessException | NumberFormatException | SQLException e) {
                JOptionPane.showMessageDialog(null, e);
            }
            textfield_id.setText("");
            textfield_name.setText("");
            textfield_amount.setText("");
        } else {
            JOptionPane.showMessageDialog(null, "All fields are mandatory.......");
        }
    }//GEN-LAST:event_updateActionPerformed

