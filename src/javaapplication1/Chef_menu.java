    private void textfield_search1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_textfield_search1KeyReleased
        ArrayList<MenuBean> al = null;
        al = new ArrayList<>();
        String val = textfield_search1.getText();
        try {
            Connection conn = OracleConnection();

            String qry = "select * from menu where name like '%" + val + "%'";
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(qry);
            MenuBean menu;
            while (rs.next()) {
                menu = new MenuBean(rs.getInt(1), rs.getString("name"), rs.getInt(3), rs.getInt(4));
                al.add(menu);
            }

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0); // Empty/clear the table
            Object[] row = new Object[4];
            for (int i = 0; i < al.size(); i++) {
                row[0] = al.get(i).getId();
                row[1] = al.get(i).getName();
                row[2] = al.get(i).getPrice();
                row[3] = al.get(i).getAmount();
                model.addRow(row);
            }
            
            conn.close();
            st.close();
            rs.close();
            
        } catch (SQLException e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_textfield_search1KeyReleased

