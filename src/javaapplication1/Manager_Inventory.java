    private void delete_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delete_buttonActionPerformed
        if (textfield_id.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Please Enter the item id");
        } else {
            try {
                Connection conn = OracleConnection();
                callstate = conn.prepareCall("{call deleteInventory(?)}");               
                int newId = Integer.parseInt(textfield_id.getText());
                callstate.setInt(1,newId);
                callstate.execute();
                fillTable();
                
                conn.close();
                callstate.close();
                
            } catch (HeadlessException | NumberFormatException | SQLException e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
        textfield_id.setText("");
        textfield_name.setText("");
        textfield_amount.setText("");
        textfield_price.setText("");
    }//GEN-LAST:event_delete_buttonActionPerformed

    private void textfield_searchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_textfield_searchKeyReleased
        ArrayList<InventoryBean> al = null;
        al = new ArrayList<>();
        String val = textfield_search.getText();
        try {
            Connection conn = OracleConnection();

            String qry = "select * from inventory where name like '%" + val + "%'";
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(qry);
            InventoryBean inventory;
            while (rs.next()) {
                inventory = new InventoryBean(rs.getInt(1), rs.getString("name"), rs.getInt(3), rs.getInt(4));
                al.add(inventory);
            }

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0); // Empty/clear the table
            Object[] row = new Object[4];
            for (int i = 0; i < al.size(); i++) {
                row[0] = al.get(i).getId();
                row[1] = al.get(i).getName();
                row[2] = al.get(i).getAmount();
                row[3] = al.get(i).getPrice();
                model.addRow(row);
            }
            
            conn.close();
            st.close();
            rs.close();
            
        } catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_textfield_searchKeyReleased

